package com.votes.myRestaurant.dto;

public class DishDTO {

    private Long id;
    private String name;

    public DishDTO(Long id, String name) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

}
